public class Car {
    private int id, cyl, disp, hp, gear, carb;
    private String name;
    private double mpg, drat, wt, qsec;
    private boolean vs, am;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getCyl() {
        return cyl;
    }

    public void setCyl(int cyl) {
        this.cyl = cyl;
    }

    public int getDisp() {
        return disp;
    }

    public void setDisp(int disp) {
        this.disp = disp;
    }

    public int getHp() {
        return hp;
    }

    public void setHp(int hp) {
        this.hp = hp;
    }

    public int getGear() {
        return gear;
    }

    public void setGear(int gear) {
        this.gear = gear;
    }

    public int getCarb() {
        return carb;
    }

    public void setCarb(int carb) {
        this.carb = carb;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getMpg() {
        return mpg;
    }

    public void setMpg(double mpg) {
        this.mpg = mpg;
    }

    public double getDrat() {
        return drat;
    }

    public void setDrat(double drat) {
        this.drat = drat;
    }

    public double getWt() {
        return wt;
    }

    public void setWt(double wt) {
        this.wt = wt;
    }

    public double getQsec() {
        return qsec;
    }

    public void setQsec(double qsec) {
        this.qsec = qsec;
    }

    public boolean isVs() {
        return vs;
    }

    public void setVs(boolean vs) {
        this.vs = vs;
    }

    public boolean isAm() {
        return am;
    }

    public void setAm(boolean am) {
        this.am = am;
    }
}
